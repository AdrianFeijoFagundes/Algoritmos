let paisDeOrigem = 'Brasil'


switch (paisDeOrigem) {
    case 'Brasil':
        console.log('Você é brasileiro')
        break
    case 'Rússia':
        console.log('Você é russo')
        break
    case 'México':
        console.log('Você é mexicano')
        break
    case 'Itália':    
        console.log('Você é italiano')
        break
    default:            
        console.log('Nacionalidade não encontrada')
}
